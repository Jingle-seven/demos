package generator.pojo;

public class SpanInfos {
    private String name;

    private String alias;

    private String defBaseline;

    private String custBaseline;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }

    public String getDefBaseline() {
        return defBaseline;
    }

    public void setDefBaseline(String defBaseline) {
        this.defBaseline = defBaseline;
    }

    public String getCustBaseline() {
        return custBaseline;
    }

    public void setCustBaseline(String custBaseline) {
        this.custBaseline = custBaseline;
    }
}